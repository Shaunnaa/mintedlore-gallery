-- Add collection_type column (type_a = full ME collection, type_b = curated sub)
ALTER TABLE communities
  ADD COLUMN IF NOT EXISTS collection_type TEXT NOT NULL DEFAULT 'type_a'
    CHECK (collection_type IN ('type_a', 'type_b'));

-- Add parent reference for type_b communities
ALTER TABLE communities
  ADD COLUMN IF NOT EXISTS parent_community_id BIGINT
    REFERENCES communities(id) ON DELETE CASCADE;

-- Prevent duplicate type_a communities for the same Magic Eden collection
CREATE UNIQUE INDEX IF NOT EXISTS unique_type_a_collection
  ON communities (collection_address)
  WHERE collection_type = 'type_a';

-- ─────────────────────────────────────────────────────────────────────────────
-- COMMUNITY_NFTS TABLE  (stores hand-picked mints for Type B communities)
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS community_nfts (
  id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  community_id    BIGINT NOT NULL REFERENCES communities(id) ON DELETE CASCADE,
  mint_address    TEXT NOT NULL,
  name            TEXT,
  image_url       TEXT,
  added_at        TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc', NOW()),
  UNIQUE(community_id, mint_address)
);

ALTER TABLE community_nfts ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Public can read community NFTs."
  ON community_nfts FOR SELECT USING (true);

CREATE POLICY "Owners can manage their community NFTs."
  ON community_nfts FOR ALL
  USING (
    EXISTS (
      SELECT 1 FROM communities c
      WHERE c.id = community_id
        AND c.owner_wallet = auth.jwt()->>'wallet'
    )
  );
